package com.cqwillia.tester.exceptions;

public class AngleExpressionException extends Exception
{
    public AngleExpressionException()
    {
        super();
    }

    public AngleExpressionException(Throwable t)
    {
        super(t);
    }

    public AngleExpressionException(String s, Throwable t)
    {
        super(s, t);
    }

    public AngleExpressionException(String s)
    {
        super(s);
    }
}
